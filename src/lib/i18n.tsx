type Ctx = {
  lang: LangCode;
  setLang: (l: LangCode) => void;
  t: (key: keyof typeof en) => string;
  rtl: boolean;
};

const I18nContext = createContext<Ctx | null>(null);

export function I18nProvider({ children }: { children: ReactNode }) {
  const [lang, setLangState] = useState<LangCode>("en");

  useEffect(() => {
    try {
      const saved = localStorage.getItem("aegis-lang") as LangCode | null;
      if (saved && T[saved]) setLangState(saved);
    } catch { /* ignore */ }
  }, []);

  useEffect(() => {
    const isRtl = LANGS.find((l) => l.code === lang)?.rtl ?? false;
    if (typeof document !== "undefined") {
      document.documentElement.lang = lang;
      document.documentElement.dir = isRtl ? "rtl" : "ltr";
    }
  }, [lang]);

  const setLang = (l: LangCode) => {
    setLangState(l);
    try { localStorage.setItem("aegis-lang", l); } catch { /* ignore */ }
  };

  const t = (key: keyof typeof en): string => T[lang]?.[key] ?? en[key] ?? String(key);
  const rtl = LANGS.find((l) => l.code === lang)?.rtl ?? false;

  return <I18nContext.Provider value={{ lang, setLang, t, rtl }}>{children}</I18nContext.Provider>;
}

export function useI18n() {
  const c = useContext(I18nContext);
  if (!c) throw new Error("useI18n must be used inside I18nProvider");
  return c;
}

export function translatedScoreLabel(t: (k: keyof typeof en) => string, label: string): string {
  if (label === "Very Safe") return t("label_very_safe");
  if (label === "Mostly Safe") return t("label_mostly_safe");
  if (label === "Caution") return t("label_caution");
  if (label === "High Risk") return t("label_high_risk");
  return label;
}
